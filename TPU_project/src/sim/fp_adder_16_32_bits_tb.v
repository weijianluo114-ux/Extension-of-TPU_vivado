module fp_adder_16_32_bits_tb;

  /******************************* 参数 ***********************************/
  localparam MATRIX_PRECISION = 0;
  localparam FP_WIDTH = 32;

  /******************************* 网表信号 ***********************************/
  wire [FP_WIDTH-1:0] C;
  wire                indicate;

  /******************************* reg信号 ***********************************/
  reg                 clk;
  reg                 rst_n;
  reg                 read_en;
  reg  [        31:0] input_A;
  reg  [        31:0] input_B;
  reg  [         5:0] input_b_precision;

  /******************************* 时序逻辑 ***********************************/
  always #5 clk = !clk;


  /******************************* 仿真过程 ***********************************/
  initial begin
    clk = 0;
    read_en = 0;
    rst_n = 0;
    input_A = 32'b0;
    input_B = 32'b0;
    input_b_precision = 0;

    #100;
    read_en = 1;
    rst_n   = 1;

    // 等待复位结束
    @(posedge clk);
    @(negedge clk);

    //--------------------------------------------------------
    // 1. 正数 + 正数
    //--------------------------------------------------------
    // 1.1  1.0 + 1.0
    input_A = 32'b0_01111111_00000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 1.2  1.0 + 2.0
    input_A = 32'b0_01111111_00000000000000000000000;
    input_B = 32'b0_10000000_00000000000000000000000;
    @(posedge clk);
    #100;

    // 1.3  0.25 + 0.5
    input_A = 32'b0_01111101_00000000000000000000000;
    input_B = 32'b0_01111110_00000000000000000000000;
    @(posedge clk);
    #100;

    // 1.4  最小规格化数 + 最大非规格化数
    input_A = 32'b0_00000001_00000000000000000000000;  // 2^-126
    input_B = 32'b0_00000000_11111111111111111111111;  // ~2^-126 - 2^-149
    @(posedge clk);
    #100;

    //--------------------------------------------------------
    // 2. 正数 + 负数
    //--------------------------------------------------------
    // 2.1  1.0 + (-1.0) = 0
    input_A = 32'b0_01111111_00000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 2.2  2.0 + (-2.0) = 0
    input_A = 32'b0_10000000_00000000000000000000000;
    input_B = 32'b1_10000000_00000000000000000000000;
    @(posedge clk);
    #100;

    // 2.3  2.0 + (-1.0) = 1.0
    input_A = 32'b0_10000000_00000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 2.4  1.5 + (-1.0) = 0.5
    input_A = 32'b0_01111111_10000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 2.5  最大非规格化数 + 负的最大非规格化数
    input_A = 32'b0_00000000_11111111111111111111111;
    input_B = 32'b1_00000000_11111111111111111111111;
    @(posedge clk);
    #100;

    //--------------------------------------------------------
    // 3. 无穷大 + 有限数
    //--------------------------------------------------------
    // 3.1  +∞ + 1.0 = +∞
    input_A = 32'b0_11111111_00000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 3.2  -∞ + (-1.0) = -∞
    input_A = 32'b1_11111111_00000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 3.3  +∞ + (-1.0) = +∞
    input_A = 32'b0_11111111_00000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 3.4  -∞ + 1.0 = -∞
    input_A = 32'b1_11111111_00000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    //--------------------------------------------------------
    // 4. 无穷大 + 无穷大
    //--------------------------------------------------------
    // 4.1  +∞ + +∞ = +∞
    input_A = 32'b0_11111111_00000000000000000000000;
    input_B = 32'b0_11111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 4.2  -∞ + -∞ = -∞
    input_A = 32'b1_11111111_00000000000000000000000;
    input_B = 32'b1_11111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 4.3  +∞ + (-∞) = NaN
    input_A = 32'b0_11111111_00000000000000000000000;
    input_B = 32'b1_11111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 4.4  -∞ + +∞ = NaN
    input_A = 32'b1_11111111_00000000000000000000000;
    input_B = 32'b0_11111111_00000000000000000000000;
    @(posedge clk);
    #100;

    //--------------------------------------------------------
    // 5. NaN + 任何数（包括 NaN）
    //--------------------------------------------------------
    // 5.1  NaN + 1.0 = NaN
    input_A = 32'b0_11111111_10000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 5.2  NaN + +∞ = NaN
    input_A = 32'b0_11111111_10000000000000000000000;
    input_B = 32'b0_11111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 5.3  NaN + NaN = NaN
    input_A = 32'b0_11111111_10000000000000000000000;
    input_B = 32'b1_11111111_10000000000000000000000;
    @(posedge clk);
    #100;

    // 5.4  带符号的 NaN + 1.0 = NaN
    input_A = 32'b1_11111111_10000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 5.5  信令 NaN + 1.0 = NaN
    input_A = 32'b0_11111111_00000000000000000000001;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    //--------------------------------------------------------
    // 6. 0 + 任何数（包括 0）
    //--------------------------------------------------------
    // 6.1  +0 + 1.0 = 1.0
    input_A = 32'b0_00000000_00000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 6.2  -0 + 1.0 = 1.0
    input_A = 32'b1_00000000_00000000000000000000000;
    input_B = 32'b0_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 6.3  +0 + (-1.0) = -1.0
    input_A = 32'b0_00000000_00000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 6.4  -0 + (-1.0) = -1.0
    input_A = 32'b1_00000000_00000000000000000000000;
    input_B = 32'b1_01111111_00000000000000000000000;
    @(posedge clk);
    #100;

    // 6.5  +0 + (-0) = +0
    input_A = 32'b0_00000000_00000000000000000000000;
    input_B = 32'b1_00000000_00000000000000000000000;
    @(posedge clk);
    #100;

    // 6.6  -0 + +0 = +0
    input_A = 32'b1_00000000_00000000000000000000000;
    input_B = 32'b0_00000000_00000000000000000000000;
    @(posedge clk);
    #100;

    //--------------------------------------------------------
    // 7. 边界/特殊组合
    //--------------------------------------------------------
    // 7.1  最小非规格化数 + 最大非规格化数
    input_A = 32'b0_00000000_00000000000000000000001;
    input_B = 32'b0_00000000_11111111111111111111111;
    @(posedge clk);
    #100;

    // 7.2  最大规格化数 + 最大规格化数（可能溢出）
    input_A = 32'b0_11111110_11111111111111111111111;
    input_B = 32'b0_11111110_11111111111111111111111;
    @(posedge clk);
    #100;

    // 7.3  最大规格化数 + 最小非规格化数
    input_A = 32'b0_11111110_11111111111111111111111;
    input_B = 32'b0_00000000_00000000000000000000001;
    @(posedge clk);
    #100;

    // 7.4  +∞ + NaN = NaN
    input_A = 32'b0_11111111_00000000000000000000000;
    input_B = 32'b0_11111111_10000000000000000000000;
    @(posedge clk);
    #100;

    // 7.5  +0 + NaN = NaN
    input_A = 32'b0_00000000_00000000000000000000000;
    input_B = 32'b0_11111111_10000000000000000000000;
    @(posedge clk);
    #100;

    // 结束仿真
    #100 $finish;


  end

  /******************************* 模块例化 ***********************************/
  fp_adder_16_32bits #(
      .MATRIX_PRECISION(MATRIX_PRECISION),
      .FP_WIDTH(FP_WIDTH)
  ) fp_adder_16_32bits_inst (
      .clk(clk),
      .rst_n(rst_n),
      .read_en(read_en),
      .input_A(input_A),
      .input_B(input_B),
      .input_b_precision(input_b_precision),
      .C(C),
      .indicate(indicate)
  );


endmodule
