module fp_adder_16_32_bits_tb;

    /******************************* 参数 ***********************************/
    localparam MATRIX_PRECISION = 0;
    localparam FP_WIDTH = 32;

    /******************************* 网表信号 ***********************************/
    wire [FP_WIDTH-1:0] C;
    wire                indicate;
    wire [        31:0] input_A;
    wire [        31:0] input_B;

    /******************************* reg信号 ***********************************/
    reg                 clk;
    reg                 rst_n;
    reg                 read_en;
    reg  [        31:0] FP32_A            [5:0];
    reg  [        31:0] FP32_B            [5:0];
    reg  [        15:0] FP16_A            [5:0];
    reg  [        15:0] FP16_B            [5:0];

    reg                 is_fp32_precision;
    reg  [         2:0] count;

    /******************************* 组合逻辑 ***********************************/
    assign input_A = (is_fp32_precision) ? FP32_A[0] : {{16{1'b0}}, FP16_A[0]};
    assign input_B = (is_fp32_precision) ? FP32_B[0] : {{16{1'b0}}, FP16_B[0]};

    /******************************* 时序逻辑 ***********************************/
    always #5 clk = !clk;


    integer i;
    //打拍传递
    always @(posedge clk) begin
        for (i = 0; i < 5; i = i + 1) begin
            FP32_A[i+1] <= FP32_A[i];
            FP32_B[i+1] <= FP32_B[i];
        end
    end

    always @(posedge clk) begin
        for (i = 0; i < 5; i = i + 1) begin
            FP16_A[i+1] <= FP16_A[i];
            FP16_B[i+1] <= FP16_B[i];
        end
    end
    /******************************* 仿真过程 ***********************************/
    initial begin
        clk = 0;
        read_en = 0;
        rst_n = 0;
        FP32_A[0] = 32'b0;
        FP32_B[0] = 32'b0;
        FP16_A[0] = 16'b0;
        FP16_B[0] = 16'b0;
        is_fp32_precision = 1;

        #100;
        read_en = 1;
        rst_n   = 1;

        // 等待复位结束
        @(posedge clk);
        @(negedge clk);

        //--------------------------------------------------------
        // 1. 正数 + 正数
        //--------------------------------------------------------
        // 1.1  1.0 + 1.0
        FP32_A[0] = 32'b0_01111111_00000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        // 1.2  1.0 + 2.0
        FP32_A[0] = 32'b0_01111111_00000000000000000000000;
        FP32_B[0] = 32'b0_10000000_00000000000000000000000;
        @(posedge clk);


        // 1.3  0.25 + 0.5
        FP32_A[0] = 32'b0_01111101_00000000000000000000000;
        FP32_B[0] = 32'b0_01111110_00000000000000000000000;
        @(posedge clk);


        // 1.4  最小规格化数 + 最大非规格化数
        FP32_A[0] = 32'b0_00000001_00000000000000000000000;  // 2^-126
        FP32_B[0] = 32'b0_00000000_11111111111111111111111;  // ~2^-126 - 2^-149
        @(posedge clk);


        //--------------------------------------------------------
        // 2. 正数 + 负数
        //--------------------------------------------------------
        // 2.1  1.0 + (-1.0) = 0
        FP32_A[0] = 32'b0_01111111_00000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 2.2  2.0 + (-2.0) = 0
        FP32_A[0] = 32'b0_10000000_00000000000000000000000;
        FP32_B[0] = 32'b1_10000000_00000000000000000000000;
        @(posedge clk);


        // 2.3  2.0 + (-1.0) = 1.0
        FP32_A[0] = 32'b0_10000000_00000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 2.4  1.5 + (-1.0) = 0.5
        FP32_A[0] = 32'b0_01111111_10000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 2.5  最大非规格化数 + 负的最大非规格化数
        FP32_A[0] = 32'b0_00000000_11111111111111111111111;
        FP32_B[0] = 32'b1_00000000_11111111111111111111111;
        @(posedge clk);


        //--------------------------------------------------------
        // 3. 无穷大 + 有限数
        //--------------------------------------------------------
        // 3.1  +∞ + 1.0 = +∞
        FP32_A[0] = 32'b0_11111111_00000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        // 3.2  -∞ + (-1.0) = -∞
        FP32_A[0] = 32'b1_11111111_00000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 3.3  +∞ + (-1.0) = +∞
        FP32_A[0] = 32'b0_11111111_00000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 3.4  -∞ + 1.0 = -∞
        FP32_A[0] = 32'b1_11111111_00000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        //--------------------------------------------------------
        // 4. 无穷大 + 无穷大
        //--------------------------------------------------------
        // 4.1  +∞ + +∞ = +∞
        FP32_A[0] = 32'b0_11111111_00000000000000000000000;
        FP32_B[0] = 32'b0_11111111_00000000000000000000000;
        @(posedge clk);


        // 4.2  -∞ + -∞ = -∞
        FP32_A[0] = 32'b1_11111111_00000000000000000000000;
        FP32_B[0] = 32'b1_11111111_00000000000000000000000;
        @(posedge clk);


        // 4.3  +∞ + (-∞) = NaN
        FP32_A[0] = 32'b0_11111111_00000000000000000000000;
        FP32_B[0] = 32'b1_11111111_00000000000000000000000;
        @(posedge clk);


        // 4.4  -∞ + +∞ = NaN
        FP32_A[0] = 32'b1_11111111_00000000000000000000000;
        FP32_B[0] = 32'b0_11111111_00000000000000000000000;
        @(posedge clk);


        //--------------------------------------------------------
        // 5. NaN + 任何数（包括 NaN）
        //--------------------------------------------------------
        // 5.1  NaN + 1.0 = NaN
        FP32_A[0] = 32'b0_11111111_10000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        // 5.2  NaN + +∞ = NaN
        FP32_A[0] = 32'b0_11111111_10000000000000000000000;
        FP32_B[0] = 32'b0_11111111_00000000000000000000000;
        @(posedge clk);


        // 5.3  NaN + NaN = NaN
        FP32_A[0] = 32'b0_11111111_10000000000000000000000;
        FP32_B[0] = 32'b1_11111111_10000000000000000000000;
        @(posedge clk);


        // 5.4  带符号的 NaN + 1.0 = NaN
        FP32_A[0] = 32'b1_11111111_10000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        // 5.5  信令 NaN + 1.0 = NaN
        FP32_A[0] = 32'b0_11111111_00000000000000000000001;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        //--------------------------------------------------------
        // 6. 0 + 任何数（包括 0）
        //--------------------------------------------------------
        // 6.1  +0 + 1.0 = 1.0
        FP32_A[0] = 32'b0_00000000_00000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        // 6.2  -0 + 1.0 = 1.0
        FP32_A[0] = 32'b1_00000000_00000000000000000000000;
        FP32_B[0] = 32'b0_01111111_00000000000000000000000;
        @(posedge clk);


        // 6.3  +0 + (-1.0) = -1.0
        FP32_A[0] = 32'b0_00000000_00000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 6.4  -0 + (-1.0) = -1.0
        FP32_A[0] = 32'b1_00000000_00000000000000000000000;
        FP32_B[0] = 32'b1_01111111_00000000000000000000000;
        @(posedge clk);


        // 6.5  +0 + (-0) = +0
        FP32_A[0] = 32'b0_00000000_00000000000000000000000;
        FP32_B[0] = 32'b1_00000000_00000000000000000000000;
        @(posedge clk);


        // 6.6  -0 + +0 = +0
        FP32_A[0] = 32'b1_00000000_00000000000000000000000;
        FP32_B[0] = 32'b0_00000000_00000000000000000000000;
        @(posedge clk);


        //--------------------------------------------------------
        // 7. 边界/特殊组合
        //--------------------------------------------------------
        // 7.1  最小非规格化数 + 最大非规格化数
        FP32_A[0] = 32'b0_00000000_00000000000000000000001;
        FP32_B[0] = 32'b0_00000000_11111111111111111111111;
        @(posedge clk);


        // 7.2  最大规格化数 + 最大规格化数（可能溢出）
        FP32_A[0] = 32'b0_11111110_11111111111111111111111;
        FP32_B[0] = 32'b0_11111110_11111111111111111111111;
        @(posedge clk);


        // 7.3  最大规格化数 + 最小非规格化数
        FP32_A[0] = 32'b0_11111110_11111111111111111111111;
        FP32_B[0] = 32'b0_00000000_00000000000000000000001;
        @(posedge clk);


        // 7.4  +∞ + NaN = NaN
        FP32_A[0] = 32'b0_11111111_00000000000000000000000;
        FP32_B[0] = 32'b0_11111111_10000000000000000000000;
        @(posedge clk);


        // 7.5  +0 + NaN = NaN
        FP32_A[0] = 32'b0_00000000_00000000000000000000000;
        FP32_B[0] = 32'b0_11111111_10000000000000000000000;
        @(posedge clk);


        #100;
        is_fp32_precision = 0;

        //--------------------------------------------------------
        // 1. 正数 + 正数
        //--------------------------------------------------------
        // 1.1  1.0 + 1.0
        FP16_A[0] = 16'b0_01111_0000000000;  // 3C00
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        // 1.2  1.0 + 2.0
        FP16_A[0] = 16'b0_01111_0000000000;  // 3C00
        FP16_B[0] = 16'b0_10000_0000000000;  // 4000
        @(posedge clk);

        // 1.3  0.25 + 0.5
        FP16_A[0] = 16'b0_01101_0000000000;  // 3400
        FP16_B[0] = 16'b0_01110_0000000000;  // 3800
        @(posedge clk);

        // 1.4  最小规格化数 + 最大非规格化数
        FP16_A[0] = 16'b0_00001_0000000000;  // 0400 (2^-14)
        FP16_B[0] = 16'b0_00000_1111111111;  // 03FF (~2^-14 - 2^-24)
        @(posedge clk);

        //--------------------------------------------------------
        // 2. 正数 + 负数
        //--------------------------------------------------------
        // 2.1  1.0 + (-1.0) = 0
        FP16_A[0] = 16'b0_01111_0000000000;  // 3C00
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 2.2  2.0 + (-2.0) = 0
        FP16_A[0] = 16'b0_10000_0000000000;  // 4000
        FP16_B[0] = 16'b1_10000_0000000000;  // C000
        @(posedge clk);

        // 2.3  2.0 + (-1.0) = 1.0
        FP16_A[0] = 16'b0_10000_0000000000;  // 4000
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 2.4  1.5 + (-1.0) = 0.5
        FP16_A[0] = 16'b0_01111_1000000000;  // 3E00
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 2.5  最大非规格化数 + 负的最大非规格化数
        FP16_A[0] = 16'b0_00000_1111111111;  // 03FF
        FP16_B[0] = 16'b1_00000_1111111111;  // 83FF
        @(posedge clk);

        //--------------------------------------------------------
        // 3. 无穷大 + 有限数
        //--------------------------------------------------------
        // 3.1  +∞ + 1.0 = +∞
        FP16_A[0] = 16'b0_11111_0000000000;  // 7C00
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        // 3.2  -∞ + (-1.0) = -∞
        FP16_A[0] = 16'b1_11111_0000000000;  // FC00
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 3.3  +∞ + (-1.0) = +∞
        FP16_A[0] = 16'b0_11111_0000000000;  // 7C00
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 3.4  -∞ + 1.0 = -∞
        FP16_A[0] = 16'b1_11111_0000000000;  // FC00
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        //--------------------------------------------------------
        // 4. 无穷大 + 无穷大
        //--------------------------------------------------------
        // 4.1  +∞ + +∞ = +∞
        FP16_A[0] = 16'b0_11111_0000000000;  // 7C00
        FP16_B[0] = 16'b0_11111_0000000000;  // 7C00
        @(posedge clk);

        // 4.2  -∞ + -∞ = -∞
        FP16_A[0] = 16'b1_11111_0000000000;  // FC00
        FP16_B[0] = 16'b1_11111_0000000000;  // FC00
        @(posedge clk);

        // 4.3  +∞ + (-∞) = NaN
        FP16_A[0] = 16'b0_11111_0000000000;  // 7C00
        FP16_B[0] = 16'b1_11111_0000000000;  // FC00
        @(posedge clk);

        // 4.4  -∞ + +∞ = NaN
        FP16_A[0] = 16'b1_11111_0000000000;  // FC00
        FP16_B[0] = 16'b0_11111_0000000000;  // 7C00
        @(posedge clk);

        //--------------------------------------------------------
        // 5. NaN + 任何数（包括 NaN）
        //--------------------------------------------------------
        // 5.1  NaN + 1.0 = NaN
        FP16_A[0] = 16'b0_11111_1000000000;  // 7E00 (qNaN)
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        // 5.2  NaN + +∞ = NaN
        FP16_A[0] = 16'b0_11111_1000000000;  // 7E00
        FP16_B[0] = 16'b0_11111_0000000000;  // 7C00
        @(posedge clk);

        // 5.3  NaN + NaN = NaN
        FP16_A[0] = 16'b0_11111_1000000000;  // 7E00
        FP16_B[0] = 16'b1_11111_1000000000;  // FE00
        @(posedge clk);

        // 5.4  带符号的 NaN + 1.0 = NaN
        FP16_A[0] = 16'b1_11111_1000000000;  // FE00
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        // 5.5  信令 NaN + 1.0 = NaN
        FP16_A[0] = 16'b0_11111_0000000001;  // 7C01 (sNaN)
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        //--------------------------------------------------------
        // 6. 0 + 任何数（包括 0）
        //--------------------------------------------------------
        // 6.1  +0 + 1.0 = 1.0
        FP16_A[0] = 16'b0_00000_0000000000;  // 0000
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        // 6.2  -0 + 1.0 = 1.0
        FP16_A[0] = 16'b1_00000_0000000000;  // 8000
        FP16_B[0] = 16'b0_01111_0000000000;  // 3C00
        @(posedge clk);

        // 6.3  +0 + (-1.0) = -1.0
        FP16_A[0] = 16'b0_00000_0000000000;  // 0000
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 6.4  -0 + (-1.0) = -1.0
        FP16_A[0] = 16'b1_00000_0000000000;  // 8000
        FP16_B[0] = 16'b1_01111_0000000000;  // BC00
        @(posedge clk);

        // 6.5  +0 + (-0) = +0
        FP16_A[0] = 16'b0_00000_0000000000;  // 0000
        FP16_B[0] = 16'b1_00000_0000000000;  // 8000
        @(posedge clk);

        // 6.6  -0 + +0 = +0
        FP16_A[0] = 16'b1_00000_0000000000;  // 8000
        FP16_B[0] = 16'b0_00000_0000000000;  // 0000
        @(posedge clk);

        //--------------------------------------------------------
        // 7. 边界/特殊组合
        //--------------------------------------------------------
        // 7.1  最小非规格化数 + 最大非规格化数
        FP16_A[0] = 16'b0_00000_0000000001;  // 0001
        FP16_B[0] = 16'b0_00000_1111111111;  // 03FF
        @(posedge clk);

        // 7.2  最大规格化数 + 最大规格化数（可能溢出）
        FP16_A[0] = 16'b0_11110_1111111111;  // 7BFF
        FP16_B[0] = 16'b0_11110_1111111111;  // 7BFF
        @(posedge clk);

        // 7.3  最大规格化数 + 最小非规格化数
        FP16_A[0] = 16'b0_11110_1111111111;  // 7BFF
        FP16_B[0] = 16'b0_00000_0000000001;  // 0001
        @(posedge clk);

        // 7.4  +∞ + NaN = NaN
        FP16_A[0] = 16'b0_11111_0000000000;  // 7C00
        FP16_B[0] = 16'b0_11111_1000000000;  // 7E00
        @(posedge clk);

        // 7.5  +0 + NaN = NaN
        FP16_A[0] = 16'b0_00000_0000000000;  // 0000
        FP16_B[0] = 16'b0_11111_1000000000;  // 7E00
        @(posedge clk);
        #100;

        // 结束仿真
        #100 $finish;


    end

    /******************************* 模块例化 ***********************************/
    fp_adder_16_32bits fp_adder_16_32bits_inst (
        .clk              (clk),
        .rst_n            (rst_n),
        .read_en          (read_en),
        .input_A_         (input_A),
        .input_B_         (input_B),
        .is_fp32_precision(is_fp32_precision),
        .C                (C),
        .indicate         (indicate)
    );


endmodule
